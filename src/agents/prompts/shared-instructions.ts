/**
 * CoT 推理框架 - 要求 Agent 在输出 JSON 前先进行结构化的推理过程
 * 参考 Chain-of-Thought Prompting 研究，引导模型逐步分析
 */
export const SHARED_COT_INSTRUCTIONS = `
## 结构化推理框架（Chain-of-Thought）

在输出最终 JSON 结果之前，你必须先进行以下逐步推理过程（思考步骤不需要输出，但必须在内部执行）：

**Step 1: 代码理解** - 通读代码，理解其整体目的、数据流和控制流。识别关键函数、入口点和依赖关系。
**Step 2: 上下文分析** - 分析代码所处的项目上下文，包括使用的框架、语言特性、设计模式。
**Step 3: 逐段审查** - 针对你负责的维度，逐段（函数/类/模块）分析代码，标记可疑之处。
**Step 4: 深度验证** - 对每个可疑点，进行深入分析：构造具体的触发条件或反例，验证该问题是否真实存在。
**Step 5: 影响评估** - 对确认的问题，评估其严重程度和实际影响，考虑发生的概率和影响范围。
**Step 6: 置信度校准** - 对每个问题的置信度进行最终校准，确保与证据强度匹配。

重要：只报告经过完整推理链验证的问题，不要报告仅凭直觉怀疑但未验证的问题。`;

/**
 * JSON 输出格式模板 - 统一的 ReviewIssue 格式定义
 * 所有维度 Agent 共用此格式，消除重复定义
 */
export const SHARED_JSON_FORMAT = `
## 输出格式

必须返回 JSON 数组，每个元素遵循以下格式：
{
  "file": "文件路径（相对路径）",
  "line": 行号（正整数）,
  "endLine": 结束行号（可选，正整数）,
  "severity": "critical" | "high" | "medium" | "low",
  "dimension": "由系统自动设定，无需手动填写",
  "description": "问题描述（必须包含：1) 问题是什么 2) 为什么是问题 3) 具体的触发条件或影响）",
  "suggestion": "修复建议（具体、可执行的改进方案）",
  "confidence": 0.0 到 1.0 之间的置信度分数,
  "codeSnippet": "相关的代码片段"
}

### 格式要求
- 仅输出 JSON 数组，不包含其他文本（不要用 markdown code fence 包裹）
- 如果没有发现问题，返回空数组 []
- 每个 issue 必须包含 file、line、severity、description、suggestion、confidence 字段`;

/**
 * 通用置信度评分指南 - 统一的 confidence 评分标准
 */
export const SHARED_CONFIDENCE_GUIDE = `
## 置信度评分指南

置信度（confidence）反映你对该问题真实存在的确信程度，必须与证据强度严格对应：

| 分数范围 | 含义 | 适用场景 |
|---------|------|---------|
| 0.9 - 1.0 | 确定 | 可以构造明确的反例或触发条件，100% 可复现 |
| 0.7 - 0.9 | 高度可能 | 有强有力的代码证据支持，只是某些运行时条件无法完全确认 |
| 0.5 - 0.7 | 中等可能 | 代码模式可疑，但取决于外部因素（如调用方行为、配置等） |
| 0.3 - 0.5 | 低可能 | 仅基于代码模式的推测，缺少直接证据 |
| < 0.3 | 极低 | 几乎是猜测，不应该报告（请在推理阶段过滤掉） |

重要：confidence < 0.3 的问题不应出现在最终输出中，请在推理阶段过滤。`;

/**
 * 通用审查原则 - 高质量审查的基本准则
 */
export const SHARED_REVIEW_PRINCIPLES = `
## 审查原则

1. **证据驱动**: 每个问题必须有具体的代码证据支撑，不接受"可能有问题"这种模糊判断
2. **具体可操作**: 修复建议必须具体到可以直接执行，而非泛泛的"需要改进"
3. **考虑上下文**: 审查代码时必须考虑其所在的项目上下文、框架约定和团队风格
4. **区分严重程度**: critical 仅用于会导致数据丢失/安全漏洞/服务崩溃的问题
5. **质量优先于数量**: 报告 3 个高质量且确定的问题，远好于 10 个不确定的猜测`;

/**
 * 反面约束 - 明确列出不应报告的误报模式
 * 参考 false positive 研究，减少审查噪音
 */
export const SHARED_FALSE_POSITIVE_CONSTRAINTS = `
## 反面约束 - 以下情况不应报告为问题（误报模式清单）

为保证审查质量，以下模式应明确排除，不要报告为问题：

1. **故意的设计选择**: 代码注释中明确说明了设计意图（如 "// 故意不处理此情况" 或 "// TODO: known limitation"），这是作者的有意决定
2. **测试代码中的特殊写法**: 测试文件中的 mock、stub、硬编码值、any 类型断言等，这些是测试的正常模式
3. **框架/库的惯用模式**: 遵循所用框架（React、Express、Vue 等）的标准惯用法，即使看起来不完美
4. **已有防护机制**: 虽然某处代码看似有问题，但上下文中已有验证/防护（如上层已做参数校验）
5. **可接受的技术债**: 带有 TODO/FIXME/HACK 注释的代码，作者已知且有计划处理
6. **配置和常量文件**: 配置文件中的硬编码值、环境变量默认值等不是"魔法数字"
7. **类型系统已保护**: TypeScript/Flow 等类型系统已经在编译期防止了该错误
8. **性能不敏感路径**: 在初始化、配置加载等只执行一次的代码路径中，微小的性能问题不值得报告
9. **向后兼容代码**: 为保持 API 兼容性而保留的废弃方法或冗余参数
10. **平台/环境特定代码**: 针对特定平台或环境的条件代码（如 browser vs node）

如果你不确定某个问题是否属于误报，请降低其 confidence（0.3-0.5），而不是完全不报告。`;

